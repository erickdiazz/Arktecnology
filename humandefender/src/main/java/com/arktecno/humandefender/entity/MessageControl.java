package com.arktecno.humandefender.entity;

public class MessageControl {
	private String deviceSource;
    private String amountOfDamage;
    private Integer deviceTarget;
    
	public String getDeviceSource() {
		return deviceSource;
	}
	public void setDeviceSource(String deviceSource) {
		this.deviceSource = deviceSource;
	}
	public String getAmountOfDamage() {
		return amountOfDamage;
	}
	public void setAmountOfDamage(String amountOfDamage) {
		this.amountOfDamage = amountOfDamage;
	}
	public Integer getDeviceTarget() {
		return deviceTarget;
	}
	public void setDeviceTarget(Integer deviceTarget) {
		this.deviceTarget = deviceTarget;
	}
    
	
}
